/* Operadores Aritméticos 

+ (soma e faz concatenação quando o número está entre aspas simples, ou seja, uma string), 
- (subtração)
/ (Divisão)
* (Multiplicação)
**(Potenciação)
% (Modulo / Resto)

Precedencias
1 -> Parentese
2 -> **
3 -> *, /, %
4 -> + , -

*/

const num1 = 5;
const num2 = 10;
console.log(num1 + num2);

// Incremento e Decremento

let contadorIncremento = 1;
contadorIncremento++;
console.log(contadorIncremento);

// Operdadores de atribuição
let contadorAtrib = 2;
contadorAtrib **= 10;
console.log(contadorAtrib);
